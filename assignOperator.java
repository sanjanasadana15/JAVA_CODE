public class assignOperator {
    public static void main(String[] args) {

        /* Assignment Operator  */
        int numb =1;
        //numb++;
        System.out.println(numb++);
        System.out.println(numb);
        System.out.println(++numb);
        System.out.println(numb);

        int numb1=1;
        System.out.println(numb1--);
        System.out.println(numb1);


    }
}
